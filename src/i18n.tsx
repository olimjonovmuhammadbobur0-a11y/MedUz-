type TranslationKey = keyof typeof translations['uz'];

interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: TranslationKey, params?: Record<string, string>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = localStorage.getItem('meduz_language');
    return (saved as Language) || 'uz';
  });

  useEffect(() => {
    localStorage.setItem('meduz_language', language);
  }, [language]);

  const t = (key: TranslationKey, params?: Record<string, string>) => {
    let str = translations[language][key] || translations['uz'][key] || key;
    if (params) {
      Object.keys(params).forEach(p => {
        str = str.replace(new RegExp(`{{${p}}}`, 'g'), params[p]);
      });
    }
    return str;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    // Fallback if used outside provider
    return {
      language: 'uz' as Language,
      setLanguage: () => {},
      t: (key: TranslationKey) => translations['uz'][key] || key
    };
  }
  return context;
};
